namespace FsMathFunctions

/// Module containing basic arithmetic operations
module BasicMath =
    /// Add two numbers
    let add x y = x + y
    
    /// Subtract y from x
    let subtract x y = x - y
    
    /// Multiply two numbers
    let multiply x y = x * y
    
    /// Divide x by y
    let divide x y = x / y
    
    /// Calculate the remainder of x divided by y
    let modulo x y = x % y
    
    /// Calculate the absolute value of a number
    let absolute x = abs x
